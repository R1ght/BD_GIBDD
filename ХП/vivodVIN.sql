create proc vivodVIN
(
@number int
)
as
SELECT Автомобиль.НомерVIN, Автомобиль.МаркаАвто as [Марка], Автомобиль.Модель, Автомобиль.Гос_Знак as [Гос. номер], Автомобиль.Регион, Клиент.Фамилия, Клиент.Имя, Клиент.Отчество 
from Автомобиль inner join Клиент on Автомобиль.Клиент = Клиент.ID_Клиента where Автомобиль.НомерVIN LIKE @number

go


EXEC vivodVIN @number = 11131;